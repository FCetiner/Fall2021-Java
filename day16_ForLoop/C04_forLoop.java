package day16_ForLoop;

public class C04_forLoop {

	public static void main(String[] args) {
		// 100’den baslayarak 50’ye(dahil) kadar olan sayilari aralarinda 
		//virgule olarak ayni satirda yazdirin
		
		for (int i = 100; i > 50; i--) {
			System.out.print(i+", ");
		}
		
			System.out.print(50);
	}

}
