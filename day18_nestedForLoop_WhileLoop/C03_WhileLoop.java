package day18_nestedForLoop_WhileLoop;

public class C03_WhileLoop {

	public static void main(String[] args) {

		//20den 30 a kadar olan sayýlarý (20 ve 30 dahil)
		//while loop ile yazdýrýn
		
		for (int i = 20; i <=30; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		
		int sayý = 20;
		
		while (sayý<=30) {
			System.out.print(sayý+" ");
			sayý++;
			
			// for loop ile çözebildiðimiz sorular while loop kullanmak
			//kodumuzu biraz uzatýr
			
		}
		

	}

}
